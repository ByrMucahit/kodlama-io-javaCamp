package Entities;

public class Campaign {
	private String whatKindGame;
	private float rateOfDiscount;
	private String nameOfCampaign;
	
	public String getWhatKindGame() {
		return whatKindGame;
	}
	public void setWhatKindGame(String whatKindGame) {
		this.whatKindGame = whatKindGame;
	}
	public float getRateOfDiscount() {
		return rateOfDiscount;
	}
	public void setRateOfDiscount(float rateOfDiscount) {
		this.rateOfDiscount = rateOfDiscount;
	}
	public String NameOfCampaign() {
		return nameOfCampaign;
	}
	public void nameOfCampaign(String nameOfCampaign) {
		this.nameOfCampaign = nameOfCampaign;
	}
}
