	public void setDepartmentInfo(String departmentInfo) {
		this.departmentInfo = departmentInfo;
	}
	public String getWhatCountryUserIsStudent() {
		return whatCountryUserIsStudent;
	}
	public void setWhatCountryUserIsStudent(String whatCountryUserIsStudent) {
		this.whatCountryUserIsStudent = whatCountryUserIsStudent;
	}
